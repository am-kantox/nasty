# English Determiners
# Articles, demonstratives, possessives, quantifiers
#
# Note: Some words (her, his, your, etc.) can be both determiners and pronouns.
# They are listed here as determiners since that's their more common usage.

~w(
  the a an this that these those
  my your his her its our their
  some any no every each either neither
  much many more most less least few several all both half
  whose
)
